# Organize by subject combo for easy access
def get_majors_by_subject(combo):
    result = {}
    for m in MAJORS_EXPANDED:
        req = m[2]  # subject_requirements
        # Check if this combo is compatible
        # e.g. '物化生' needs 物+化+生; '物化' needs 物+化
        if combo == '不限':
            if req == '不限':
                result[m[0]] = m
            continue
        # For subject combos like '物化生', check compatibility
        # '物化生' matches '物化生', '物化', '物生', '化生', '不限'
        required_chars = set(req)
        combo_chars = set(combo)
        if req == '不限':
            result[m[0]] = m
        elif required_chars.issubset(combo_chars):
            result[m[0]] = m
    return result

