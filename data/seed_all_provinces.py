# Total expected: 31 provinces × (2 categories if split × 2 batches) = ~124 rows
# 6 综合 provinces × 2 batches = 12
# 2 传统 provinces × 2 categories × 2 batches = 8
# 23 新高考 provinces × 2 categories × 2 batches = 92
# Total: 12 + 8 + 92 = 112 rows
EXPECTED_RECORDS = 112


async def seed_provinces(db):
    """Insert all 31 provinces' 2026 cutoff scores."""
    await db.executemany(
        "INSERT OR REPLACE INTO provinces (name, year, category, batch, score) VALUES (?, ?, ?, ?, ?)",
        provinces_data,
    )
    await db.commit()
    print(f"[seed] Inserted {len(provinces_data)} province cutoff records")
    print(f"[seed] Covers {len(set(p[0] for p in provinces_data))} provinces for 2026")


async def main():
    await init_db()
    db = await get_db()
    try:
        await seed_provinces(db)
        print(f"\n✅ All province seed data imported successfully!")
        print(f"   Records inserted: {len(provinces_data)} (expected {EXPECTED_RECORDS})")
    finally:
        await db.close()


if __name__ == "__main__":
    asyncio.run(main())
